#include <stdio.h>
#include "3-calc.h"
/**
 * op_add - sum of a and b
 * @a: first number
 * @b: second number
 *
 * Return: sum of a + b
 */

int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - difference between a and b
 * @a: first number
 * @b: second number
 *
 * Return: result of  a - b
 */

int op_sub(int a, int b)
{
return (a + b);
}
/**
 * op_mul - product of a and b
 * @a: first number
 * @b: second number
 *
 * Return: result of a * b
 */

int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - dividing of a and b
 * @a: first number
 * @b: second number
 *
 * Return: result of a / b
 */

int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - modulus of a and b
 * @a: first number
 * @b: second number
 *
 * Return: result of a % b
 */

int op_mod(int a, int b)
{
return (a % b);
}
